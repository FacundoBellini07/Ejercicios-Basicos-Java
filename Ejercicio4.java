public class Ejercicio4 {

    public static void main(String[] args) {
        String[] frutas = {"Manzana", "Banana", "Naranja", "Pera", "Sandia"}; // Defino un arreglo con distintos tipos de frutas

        for (String fruta : frutas) { // la variable fruta almacenará cada elemento de frutas una por una con cada iteración
            System.out.println(fruta); // se imprime el valor actual de la variable fruta
        }
    }
}